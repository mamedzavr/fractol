#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	int			len;
	char		*str;

	if (!s || !f)
		return (NULL);
	len = ft_strlen(s);
	str = (char *)malloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	while (*s)
		*str++ = f(*s++);
	*str = '\0';
	str = str - len;
	return (str);
}
